package B_01_inout_arithmetic;

import java.util.Scanner;

public class B1_07_10998_A_Multiplication_B {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		
		int a = scan.nextInt();
		int b = scan.nextInt();
		
		System.out.println(a*b);
		
		int c = scan.nextInt();
		int d = scan.nextInt();
		
		System.out.println(c*d);
	}
}
